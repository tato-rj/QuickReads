<script>
function ValidateEmail(email){

  var testEmail = /^[A-Z0-9._%+-]+@([A-Z0-9-]+\.)+[A-Z]{2,4}$/i;
  
  if (testEmail.test(email)) {
      return true;
  } else {
    return false;
  }

}



$('form input[name="submit"]').on('click', function(event){
  event.preventDefault();
  var $input = $('form input[name="email"]').val();
  
  if (ValidateEmail($input)) {

    var $email = $('form input[name="email"]').val();
    var $page_id = $('form input[name="email"]').attr('page_id');

    $('form > div').removeAttr('style');

    $.ajax({
          url     : "_files/mail/sendmail.php",
          type    : "POST",
          data    : {
            email: $email,
            page_id: $page_id
          },
          success : function() {  
        $('form input[name="email"]').fadeOut('fast');
      
        $($('form input[name="submit"]')).animate({
          width: '96%'
        }, {
          easing: 'linear',
          duration: 200,
          complete: function(){
            $(this).attr('disabled', true).delay(500).val('Thank you!');
          }
        });
          },
      }); 

  } else {

    $('.notValid').fadeIn();
    setTimeout(
      function() 
      {
        $('.notValid').fadeOut();
      }, 2000);
  }
  
});
</script>
